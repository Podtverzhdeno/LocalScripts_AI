@mcp.resource("sessions://list")
async def list_sessions() -> str:
    """List all LocalScript workspace sessions with their status."""
    workspace = _get_workspace()
    if not workspace.exists():
        return "No sessions found."

    lines = []
    for entry in sorted(workspace.iterdir(), reverse=True):
        if not entry.is_dir() or not entry.name.startswith("session_"):
            continue
        task_file = entry / "task.txt"
        task = task_file.read_text(encoding="utf-8").strip() if task_file.exists() else "(no task)"
        has_final = (entry / "final.lua").exists()
        status = "✓ done" if has_final else "⏳ pending"
        lines.append(f"{entry.name}  [{status}]  {task[:60]}")

    return "\n".join(lines) if lines else "No sessions found."


@mcp.resource("session://{session_id}/status")
async def get_session_status(session_id: str) -> str:
    """Get detailed status of a specific session."""
    session_path = _get_workspace() / session_id
    if not session_path.is_dir():
        return f"Session '{session_id}' not found."

    task_file = session_path / "task.txt"
    task = task_file.read_text(encoding="utf-8").strip() if task_file.exists() else "(no task)"
    iterations = len(list(session_path.glob("iteration_*.lua")))
    has_final = (session_path / "final.lua").exists()

    # Collect error files
    error_files = sorted(session_path.glob("iteration_*_errors.txt"))
    last_error = ""
    if error_files:
        last_error = error_files[-1].read_text(encoding="utf-8").strip()

    report = session_path / "report.md"
    report_text = report.read_text(encoding="utf-8") if report.exists() else ""

    return f"""Session: {session_id}
Task: {task}
Iterations: {iterations}
Final: {'available' if has_final else 'not yet'}
Last error: {last_error or '(none)'}

{report_text}"""


@mcp.resource("session://{session_id}/final.lua")
async def get_final_lua(session_id: str) -> str:
    """Get the final generated Lua code from a session."""
    final_path = _get_workspace() / session_id / "final.lua"
    if not final_path.exists():
        return f"No final.lua found for session '{session_id}'. Pipeline may still be running or failed."
    return final_path.read_text(encoding="utf-8")


