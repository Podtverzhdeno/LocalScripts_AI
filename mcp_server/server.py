def _get_workspace() -> Path:
    from config.loader import load_settings
    settings = load_settings()
    return Path(settings["workspace"]["base_dir"])


def _make_session_dir() -> tuple[str, Path]:
    """Create a timestamped session directory."""
    workspace = _get_workspace()
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    session_id = f"session_{timestamp}"
    session_path = workspace / session_id
    session_path.mkdir(parents=True, exist_ok=True)
    return session_id, session_path


def _run_pipeline_sync(task: str, session_dir: str, max_iterations: int = 3) -> dict:
    """Run the LangGraph pipeline synchronously."""
    from graph.pipeline import run_pipeline
    return run_pipeline(task=task, session_dir=session_dir, max_iterations=max_iterations)


