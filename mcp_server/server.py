@mcp.tool()
async def generate_lua(task: str, max_iterations: int = 3) -> str:
    """Generate validated Lua code using the multi-agent pipeline (Generator → Validator → Reviewer).

    The code goes through multiple agents:
    1. Generator writes Lua code based on the task description
    2. Validator compiles and executes it with luac/lua
    3. Reviewer checks code quality and correctness

    If validation fails, the pipeline retries up to max_iterations times.

    Args:
        task: Description of what the Lua code should do (e.g. "fibonacci with memoization")
        max_iterations: Maximum retry attempts if validation fails (default: 3)

    Returns:
        Generated Lua code if successful, or error description if failed.
    """
    logger.info("generate_lua: task=%s, max_iter=%d", task[:80], max_iterations)

    session_id, session_path = _make_session_dir()
    (session_path / "task.txt").write_text(task, encoding="utf-8")

    loop = asyncio.get_running_loop()
    try:
        final_state = await loop.run_in_executor(
            None,
            lambda: _run_pipeline_sync(task, str(session_path), max_iterations),
        )
    except Exception as e:
        logger.exception("Pipeline error")
        return f"ERROR: Pipeline failed — {e}"

    status = final_state.get("status", "failed")
    code = final_state.get("code", "")
    iterations = final_state.get("iterations", 0)

    if status == "done" and code:
        logger.info("generate_lua: SUCCESS in %d iteration(s), session=%s", iterations, session_id)
        return f"-- Generated by LocalScript ({iterations} iteration(s), session: {session_id})\n\n{code}"
    else:
        errors = final_state.get("errors", "Unknown error")
        return f"FAILED after {iterations} iteration(s).\nSession: {session_id}\nErrors: {errors}\nPartial code:\n{code or '(none)'}"


@mcp.tool()
async def validate_lua(code: str) -> str:
    """Validate Lua code by compiling with luac and executing with lua.

    Does NOT generate code — only checks existing code for syntax and runtime errors.

    Args:
        code: Lua source code to validate.

    Returns:
        "VALID" with stdout if code runs successfully, or error details if it fails.
    """
    logger.info("validate_lua: %d chars", len(code))

    from tools.lua_runner import LuaRunner

    with tempfile.TemporaryDirectory() as tmpdir:
        runner = LuaRunner(tmpdir)
        result = runner.execute(code, iteration=1)

    if result.success:
        output = result.stdout.strip()
        return f"VALID\n{f'Output: {output}' if output else '(no output)'}"
    else:
        return f"INVALID\nErrors: {result.errors}"


@mcp.tool()
async def review_lua(code: str, task: str = "") -> str:
    """Review Lua code quality using the Reviewer AI agent.

    Checks correctness, Lua idioms, error handling, and readability.
    Does NOT modify the code — only provides feedback.

    Args:
        code: Lua source code to review.
        task: Optional task description for context (what the code should do).

    Returns:
        Review feedback from the AI reviewer agent.
    """
    logger.info("review_lua: %d chars, task=%s", len(code), task[:50])

    from llm.factory import get_llm
    from agents.reviewer import ReviewerAgent

    loop = asyncio.get_running_loop()
    llm = get_llm("reviewer")
    agent = ReviewerAgent(llm)

    is_done, feedback = await loop.run_in_executor(
        None,
        lambda: agent.review(code=code, task=task),
    )

    status = "APPROVED" if is_done else "NEEDS IMPROVEMENT"
    return f"{status}\n\n{feedback}"


@mcp.tool()
async def fix_lua(code: str, errors: str, task: str = "") -> str:
    """Fix broken Lua code given the error messages.

    Takes existing code and its error output, returns corrected code.

    Args:
        code: Broken Lua source code.
        errors: Error messages from luac/lua or description of what's wrong.
        task: Optional task description for context.

    Returns:
        Fixed Lua source code.
    """
    logger.info("fix_lua: %d chars code, %d chars errors", len(code), len(errors))

    from llm.factory import get_llm
    from agents.generator import GeneratorAgent

    loop = asyncio.get_running_loop()
    llm = get_llm("generator")
    agent = GeneratorAgent(llm)

    fixed_code = await loop.run_in_executor(
        None,
        lambda: agent.generate(task=task or "Fix the Lua code", errors=errors),
    )

    return fixed_code


